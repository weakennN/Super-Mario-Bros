package Game.Levels;

public class WorldBackground {

    private double sizeX;
    private double sizeY;

    public WorldBackground(double sizeX, double sizeY) {
        this.sizeX = sizeX;
        this.sizeY = sizeY;
    }

    public double getSizeX() {
        return sizeX;
    }

    public double getSizeY() {
        return sizeY;
    }
}
